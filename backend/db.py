def _compute_config_hash(yaml_path: Path) -> str:
    """SHA-256 of the parsed YAML config (not raw bytes).

    Hashing parsed config means whitespace/comment changes don't
    invalidate the version — only meaningful value changes do.
    """
    if not yaml_path.exists():
        return "no-config"
    try:
        import yaml
        with open(yaml_path, "r") as f:
            parsed = yaml.safe_load(f) or {}
        normalized = json.dumps(parsed, sort_keys=True, default=str)
        return hashlib.sha256(normalized.encode()).hexdigest()[:16]
    except Exception:
        return hashlib.sha256(yaml_path.read_bytes()).hexdigest()[:16]


def _run_migrations(conn: sqlite3.Connection, from_version: int, to_version: int) -> None:
    """Run forward-only schema migrations.

    Each migration is a function that takes a connection and applies
    ALTER TABLE / CREATE TABLE / CREATE INDEX statements.
    New migrations go at the bottom with the next version number.
    """
    if from_version < 2:
        # v2: persistent walk-forward replay cache. Hashes of universe + rules
        # let us auto-invalidate when paper_portfolios.yaml changes;
        # market_data_date gives us natural daily rollover.
        conn.executescript("""
            CREATE TABLE IF NOT EXISTS replay_cache (
                lane_id TEXT NOT NULL,
                universe_hash TEXT NOT NULL,
                rules_hash TEXT NOT NULL,
                market_data_date TEXT NOT NULL,
                computed_at TEXT NOT NULL,
                result_json TEXT NOT NULL,
                PRIMARY KEY (lane_id, universe_hash, rules_hash, market_data_date)
            );
            CREATE INDEX IF NOT EXISTS idx_replay_cache_lane
                ON replay_cache (lane_id, computed_at DESC);
        """)

    if from_version < 3:
        # v3: (a) paper_nav — daily mark-to-market NAV for the live forward track
        # record, segmented by config_version so optimization/rule changes that
        # land as versioned config edits don't contaminate the prior segment.
        # (b) rule_experiments — the Aegis-OWNED trial registry for the guarded
        # rule-evolution loop. DSR/PBO must deflate against the CUMULATIVE trial
        # count across ALL runs (read from this table), not per-batch, or the
        # loop slowly overfits. Optimus may ingest this as corpus; it never owns it.
        conn.executescript("""
            CREATE TABLE IF NOT EXISTS paper_nav (
                portfolio_id TEXT NOT NULL REFERENCES paper_portfolios(id),
                date TEXT NOT NULL,              -- ISO date of the NAV mark
                nav REAL NOT NULL,               -- mark-to-market portfolio value
                config_version TEXT NOT NULL,    -- SHA of config producing this NAV
                computed_at TEXT NOT NULL,
                PRIMARY KEY (portfolio_id, date)
            );
            CREATE INDEX IF NOT EXISTS idx_paper_nav_lane
                ON paper_nav (portfolio_id, date);

            CREATE TABLE IF NOT EXISTS rule_experiments (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                created_at TEXT NOT NULL,
                config_version TEXT NOT NULL,
                lane_id TEXT,
                param TEXT NOT NULL,             -- rule param tested (e.g. drift threshold)
                old_value TEXT,                  -- JSON
                new_value TEXT,                  -- JSON
                observed_sharpe REAL,
                n_obs INTEGER,
                batch_trials INTEGER NOT NULL,   -- candidates in this batch
                cumulative_trials INTEGER NOT NULL,  -- total trials AT this point
                dsr REAL,                        -- deflated against cumulative_trials
                pbo REAL,
                verdict TEXT NOT NULL,           -- 'adopted' | 'rejected'
                notes TEXT
            );
            CREATE INDEX IF NOT EXISTS idx_rule_experiments_created
                ON rule_experiments (created_at DESC);
        """)

    if from_version < 4:
        # v4: rebalance_events carries the config_version that produced it, so
        # a versioned config change (Step #2 v1→v2) stamps its segment boundary
        # on the event itself, not only on subsequent paper_nav rows.
        # Additive column only — historical rows keep NULL (pre-v4 events).
        conn.executescript("""
            ALTER TABLE rebalance_events ADD COLUMN config_version TEXT;
        """)

    if from_version < 5:
        # v5: rule_experiments carries the participation-ratio effective trial
        # count (N_eff) alongside the raw cumulative count. REPORTED, NOT gating
        # — the adoption guard still deflates against the raw count (a strictness
        # floor); N_eff is the "estimated independent-trials" view for audit.
        # Additive column only — historical rows keep NULL. Guarded: rule_experiments
        # was created in the <3 block, so skip cleanly if a db never had it, and
        # stay idempotent if the column is already present.
        has_table = conn.execute(
            "SELECT name FROM sqlite_master WHERE type='table' AND name='rule_experiments'"
        ).fetchone()
        if has_table:
            cols = [r[1] for r in conn.execute(
                "PRAGMA table_info(rule_experiments)"
            ).fetchall()]
            if "effective_trials" not in cols:
                conn.execute(
                    "ALTER TABLE rule_experiments ADD COLUMN effective_trials REAL"
                )

    if from_version < 6:
        # v6: personal_decisions.late_entry — a decision logged after the fact
        # (the action already happened). timestamp stays server-now (never
        # backdated); late_entry just flags it for honest attribution. Additive
        # nullable column; ALTER ADD COLUMN does not fire the immutability triggers.
        has_pd = conn.execute(
            "SELECT name FROM sqlite_master WHERE type='table' AND name='personal_decisions'"
        ).fetchone()
        if has_pd:
            pcols = [r[1] for r in conn.execute(
                "PRAGMA table_info(personal_decisions)"
            ).fetchall()]
            if "late_entry" not in pcols:
                conn.execute(
                    "ALTER TABLE personal_decisions ADD COLUMN late_entry INTEGER DEFAULT 0"
                )

    if from_version < 7:
        # v7: pit_observations — the point-in-time / as-of data store (V3 linchpin,
        # see docs/V3_DATA_LAYER_DESIGN.md). Every external datum is recorded with
        # BOTH the date it refers to (as_of) AND the date WE first saw it
        # (observed_at). Reads filter observed_at <= decision_ts, so a revised
        # series can never leak the future into a backtest or lane feedback. Never
        # overwrite: a changed value is a new row (revision+1). Fully independent of
        # paper_nav — nothing here touches the track-record write-path.
        conn.executescript("""
            CREATE TABLE IF NOT EXISTS pit_observations (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                key TEXT NOT NULL,              -- e.g. 'fred:CPIAUCSL', '13f:BRK:AAPL', 'ipo:count:weekly'
                as_of TEXT NOT NULL,            -- ISO date the value refers to
                observed_at TEXT NOT NULL,      -- ISO ts we recorded it (server clock; anti-leak)
                value REAL,                     -- numeric payload (NULL if structured-only)
                payload TEXT,                   -- optional JSON for non-scalar values
                source TEXT NOT NULL,           -- 'fred' | 'edgar' | 'scrape:fearandgreed' | ...
                revision INTEGER NOT NULL DEFAULT 0,
                UNIQUE(key, as_of, observed_at)
            );
            CREATE INDEX IF NOT EXISTS idx_pit_key_asof
                ON pit_observations (key, as_of);
            CREATE INDEX IF NOT EXISTS idx_pit_key_observed
                ON pit_observations (key, observed_at);
        """)


def init_db(db_path: Path | None = None) -> None:
    """Create tables and run forward-only migrations.

    Safe to call multiple times — uses CREATE TABLE IF NOT EXISTS.
    """
    path = db_path or DB_PATH
    path.parent.mkdir(parents=True, exist_ok=True)

    conn = sqlite3.connect(str(path))
    try:
        conn.execute("PRAGMA journal_mode=WAL")
        conn.execute("PRAGMA foreign_keys=ON")

        conn.executescript(_SCHEMA_V1)

        row = conn.execute("SELECT MAX(version) FROM _schema_version").fetchone()
        current = row[0] if row[0] is not None else 0

        if current < CURRENT_SCHEMA_VERSION:
            _run_migrations(conn, current, CURRENT_SCHEMA_VERSION)
            conn.execute(
                "INSERT INTO _schema_version (version) VALUES (?)",
                (CURRENT_SCHEMA_VERSION,),
            )
            conn.commit()
            logger.info("Database initialized at schema version %d: %s", CURRENT_SCHEMA_VERSION, path)
        else:
            logger.debug("Database already at schema version %d", current)
    finally:
        conn.close()


def get_connection(db_path: Path | None = None) -> sqlite3.Connection:
    """Get a SQLite connection with standard pragmas.

    Callers are responsible for closing. Use as context manager:
        with get_connection() as conn:
            conn.execute(...)
    """
    path = db_path or DB_PATH
    conn = sqlite3.connect(str(path))
    conn.execute("PRAGMA journal_mode=WAL")
    conn.execute("PRAGMA foreign_keys=ON")
    conn.row_factory = sqlite3.Row
    return conn


def get_config_hash() -> str:
    """Hash of the current paper_portfolios.yaml."""
    return _compute_config_hash(BACKEND_DIR / "data" / "paper_portfolios.yaml")


# ── Point-in-time (as-of) data store — v7 (docs/V3_DATA_LAYER_DESIGN.md) ──────


def _utc_now_iso() -> str:
    return datetime.now(timezone.utc).isoformat()


def _pit_row_to_dict(row) -> dict | None:
    """Map a pit_observations row (positional, row_factory-agnostic) to a dict,
    decoding the JSON payload. Returns None for a None row."""
    if row is None:
        return None
    return {
        "key": row[0],
        "as_of": row[1],
        "observed_at": row[2],
        "value": row[3],
        "payload": json.loads(row[4]) if row[4] else None,
        "source": row[5],
        "revision": row[6],
    }


def snapshot(
    conn: sqlite3.Connection,
    key: str,
    as_of: str,
    value: float | None = None,
    *,
    source: str,
    payload: dict | None = None,
    observed_at: str | None = None,
) -> int | None:
    """Record a point-in-time observation. Returns the new row id, or None if the
    value+payload are unchanged from the latest known revision for (key, as_of).

    - ``as_of``: the date the value refers to (e.g. a CPI print's reference month).
    - ``observed_at``: when WE recorded it (defaults to UTC now). The anti-leak
      field — leak-free reads filter ``observed_at <= decision_ts``.
    - Never overwrites: a changed value inserts a new row with ``revision + 1``.
      An unchanged value is a no-op (so collectors can run every tick cheaply).
    """
    observed = observed_at or _utc_now_iso()
    payload_json = json.dumps(payload, sort_keys=True) if payload is not None else None
    with _write_lock:
        latest = conn.execute(
            """SELECT value, payload, revision FROM pit_observations
               WHERE key = ? AND as_of = ?
               ORDER BY revision DESC, observed_at DESC LIMIT 1""",
            (key, as_of),
        ).fetchone()
        if latest is not None:
            prev_value, prev_payload, prev_rev = latest[0], latest[1], latest[2]
            if prev_value == value and prev_payload == payload_json:
                return None  # unchanged — don't store a duplicate
            revision = prev_rev + 1
        else:
            revision = 0
        cursor = conn.execute(
            """INSERT INTO pit_observations
               (key, as_of, observed_at, value, payload, source, revision)
               VALUES (?, ?, ?, ?, ?, ?, ?)""",
            (key, as_of, observed, value, payload_json, source, revision),
        )
        conn.commit()
        return cursor.lastrowid


def get_latest_observable(
    conn: sqlite3.Connection, key: str, as_of_ts: str | None = None
) -> dict | None:
    """The single value we WOULD have known for ``key`` at ``as_of_ts`` (leak-free):
    the greatest ``as_of`` among rows whose ``observed_at <= as_of_ts``
    (tie-break latest observed_at). ``as_of_ts=None`` → the latest live value."""
    cutoff = as_of_ts or _utc_now_iso()
    row = conn.execute(
        """SELECT key, as_of, observed_at, value, payload, source, revision
           FROM pit_observations
           WHERE key = ? AND observed_at <= ?
           ORDER BY as_of DESC, observed_at DESC, revision DESC
           LIMIT 1""",
        (key, cutoff),
    ).fetchone()
    return _pit_row_to_dict(row)


def get_series_observable(
    conn: sqlite3.Connection, key: str, as_of_ts: str | None = None
) -> list[dict]:
    """The full series for ``key`` as it was knowable at ``as_of_ts``: one row per
    ``as_of``, each the latest revision with ``observed_at <= as_of_ts``, ordered
    by ``as_of`` ascending. The leak-free input for computing indicators."""
    cutoff = as_of_ts or _utc_now_iso()
    rows = conn.execute(
        """SELECT key, as_of, observed_at, value, payload, source, revision
           FROM pit_observations p
           WHERE key = ? AND observed_at <= ?
             AND observed_at = (
                 SELECT MAX(observed_at) FROM pit_observations
                 WHERE key = p.key AND as_of = p.as_of AND observed_at <= ?
             )
           ORDER BY as_of ASC""",
        (key, cutoff, cutoff),
    ).fetchall()
    return [_pit_row_to_dict(r) for r in rows]


def get_revisions(conn: sqlite3.Connection, key: str, as_of: str) -> list[dict]:
    """All recorded revisions for a specific (key, as_of), oldest first (audit)."""
    rows = conn.execute(
        """SELECT key, as_of, observed_at, value, payload, source, revision
           FROM pit_observations WHERE key = ? AND as_of = ?
           ORDER BY revision ASC, observed_at ASC""",
        (key, as_of),
    ).fetchall()
    return [_pit_row_to_dict(r) for r in rows]


def insert_rebalance_event(
    conn: sqlite3.Connection,
    portfolio_id: str,
    triggered_at: str,
    trigger_reason: str,
    pre_weights: dict,
    post_weights: dict,
    crash_prob_3m: float | None,
    regime: str | None,
    explanation: str,
    config_version: str | None = None,
) -> int:
    """Insert a rebalance event. Returns the row id.

    config_version stamps which config produced the event (v4+); pass
    get_config_hash() from live paths so segment boundaries are auditable
    on the event itself.
    """
    with _write_lock:
        cursor = conn.execute(
            """INSERT INTO rebalance_events
               (portfolio_id, triggered_at, trigger_reason, pre_weights, post_weights,
                crash_prob_3m, regime, explanation, config_version)
               VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)""",
            (
                portfolio_id,
                triggered_at,
                trigger_reason,
                json.dumps(pre_weights),
                json.dumps(post_weights),
                crash_prob_3m,
                regime,
                explanation,
                config_version,
            ),
        )
        conn.commit()
        return cursor.lastrowid


def insert_audit_log(
    conn: sqlite3.Connection,
    timestamp: str,
    portfolio_id: str | None,
    event_type: str,
    payload: dict,
) -> int:
    """Insert an audit log entry. Returns the row id."""
    with _write_lock:
        cursor = conn.execute(
            """INSERT INTO audit_log (timestamp, portfolio_id, event_type, payload)
               VALUES (?, ?, ?, ?)""",
            (timestamp, portfolio_id, event_type, json.dumps(payload)),
        )
        conn.commit()
        return cursor.lastrowid


def _hash_dict(d: dict | list | None) -> str:
    """Stable SHA-256 of a JSON-serializable structure (sorted keys)."""
    if not d:
        return "empty"
    return hashlib.sha256(
        json.dumps(d, sort_keys=True, default=str).encode()
    ).hexdigest()[:16]


def compute_universe_hash() -> str:
    """Hash of paper_portfolios.universe (changes when ticker universe changes)."""
    from backend.config import paper_portfolios
    return _hash_dict(paper_portfolios.get("universe"))


def compute_rules_hash(lane_id: str) -> str:
    """Hash of a single lane's rule config (changes when thresholds/weights change).

    paper_portfolios.yaml stores each lane as a top-level key alongside 'universe'.
    """
    from backend.config import paper_portfolios
    lane_cfg = paper_portfolios.get(lane_id)
    return _hash_dict(lane_cfg)


def get_cached_replay(
    lane_id: str,
    universe_hash: str,
    rules_hash: str,
    market_data_date: str,
) -> str | None:
    """Return cached result_json string or None on miss."""
    conn = get_connection()
    try:
        row = conn.execute(
            """SELECT result_json FROM replay_cache
               WHERE lane_id = ? AND universe_hash = ? AND rules_hash = ?
                 AND market_data_date = ?""",
            (lane_id, universe_hash, rules_hash, market_data_date),
        ).fetchone()
        return row["result_json"] if row else None
    finally:
        conn.close()


def save_cached_replay(
    lane_id: str,
    universe_hash: str,
    rules_hash: str,
    market_data_date: str,
    result_json: str,
) -> None:
    """Upsert a cache entry. Last writer wins for the same key."""
    from datetime import datetime as _dt
    with _write_lock:
        conn = get_connection()
        try:
            conn.execute(
                """INSERT OR REPLACE INTO replay_cache
                   (lane_id, universe_hash, rules_hash, market_data_date,
                    computed_at, result_json)
                   VALUES (?, ?, ?, ?, ?, ?)""",
                (
                    lane_id,
                    universe_hash,
                    rules_hash,
                    market_data_date,
                    _dt.utcnow().isoformat(),
                    result_json,
                ),
            )
            conn.commit()
        finally:
            conn.close()


def invalidate_replay_cache(lane_id: str | None = None) -> int:
    """Drop cache rows. lane_id=None clears everything. Returns rows deleted."""
    with _write_lock:
        conn = get_connection()
        try:
            if lane_id is None:
                cursor = conn.execute("DELETE FROM replay_cache")
            else:
                cursor = conn.execute(
                    "DELETE FROM replay_cache WHERE lane_id = ?", (lane_id,)
                )
            conn.commit()
            return cursor.rowcount
        finally:
            conn.close()


def get_latest_cached_replay(lane_id: str) -> tuple[str, str] | None:
    """Most recent cache row for a lane regardless of hash (for stale-fallback).

    Returns (result_json, computed_at) or None.
    """
    conn = get_connection()
    try:
        row = conn.execute(
            """SELECT result_json, computed_at FROM replay_cache
               WHERE lane_id = ? ORDER BY computed_at DESC LIMIT 1""",
            (lane_id,),
        ).fetchone()
        if not row:
            return None
        return (row["result_json"], row["computed_at"])
    finally:
        conn.close()


def insert_nav(
    conn: sqlite3.Connection,
    portfolio_id: str,
    nav_date: str,
    nav: float,
    config_version: str,
    computed_at: str,
) -> None:
    """Upsert a daily mark-to-market NAV point (last writer wins per date)."""
    with _write_lock:
        conn.execute(
            """INSERT OR REPLACE INTO paper_nav
               (portfolio_id, date, nav, config_version, computed_at)
               VALUES (?, ?, ?, ?, ?)""",
            (portfolio_id, nav_date, nav, config_version, computed_at),
        )
        conn.commit()


def get_nav_series(
    conn: sqlite3.Connection,
    portfolio_id: str,
    config_version: str | None = None,
) -> list[dict]:
    """Return [{date, nav, config_version}] ordered by date.

    Pass config_version to fetch only one track-record segment (so a versioned
    rule/optimization change starts a clean segment instead of contaminating
    the prior one).
    """
    if config_version is None:
        rows = conn.execute(
            "SELECT date, nav, config_version FROM paper_nav "
            "WHERE portfolio_id = ? ORDER BY date",
            (portfolio_id,),
        ).fetchall()
    else:
        rows = conn.execute(
            "SELECT date, nav, config_version FROM paper_nav "
            "WHERE portfolio_id = ? AND config_version = ? ORDER BY date",
            (portfolio_id, config_version),
        ).fetchall()
    return [{"date": r["date"], "nav": r["nav"],
             "config_version": r["config_version"]} for r in rows]


def count_cumulative_trials(conn: sqlite3.Connection) -> int:
    """Total rule-evolution trials EVER recorded.

    This is the multiple-testing count the DSR/PBO guards must deflate against —
    cumulative across all loop runs, not per-batch. See record_trial /
    experiment_registry for the guarded acceptance logic.
    """
    row = conn.execute("SELECT COUNT(*) AS n FROM rule_experiments").fetchone()
    return int(row["n"]) if row else 0


def insert_experiment(
    conn: sqlite3.Connection,
    created_at: str,
    config_version: str,
    param: str,
    batch_trials: int,
    cumulative_trials: int,
    verdict: str,
    *,
    lane_id: str | None = None,
    old_value=None,
    new_value=None,
    observed_sharpe: float | None = None,
    n_obs: int | None = None,
    dsr: float | None = None,
    pbo: float | None = None,
    effective_trials: float | None = None,
    notes: str | None = None,
) -> int:
    """Record one rule-evolution trial in the registry. Returns the row id."""
    with _write_lock:
        cursor = conn.execute(
            """INSERT INTO rule_experiments
               (created_at, config_version, lane_id, param, old_value, new_value,
                observed_sharpe, n_obs, batch_trials, cumulative_trials,
                dsr, pbo, effective_trials, verdict, notes)
               VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)""",
            (
                created_at, config_version, lane_id, param,
                json.dumps(old_value) if old_value is not None else None,
                json.dumps(new_value) if new_value is not None else None,
                observed_sharpe, n_obs, batch_trials, cumulative_trials,
                dsr, pbo, effective_trials, verdict, notes,
            ),
        )
        conn.commit()
        return cursor.lastrowid


def insert_personal_decision(
    conn: sqlite3.Connection,
    timestamp: str,
    ticker: str,
    action: str,
    shares_delta: float,
    price: float,
    rationale: str,
    thesis_tags: list[str],
    conviction: int,
    portfolio_snapshot: dict,
    target_price: float | None = None,
    stop_price: float | None = None,
    planned_exit_trigger: str | None = None,
    catalyst_dates: list[str] | None = None,
    amends_id: int | None = None,
    late_entry: bool = False,
) -> int:
    """Insert a personal decision. Immutable — edits create new rows via amends_id.

    Raises ValueError if rationale is too short.
    """
    if len(rationale) < 50:
        raise ValueError(f"Rationale must be >= 50 characters (got {len(rationale)})")
    if conviction < 1 or conviction > 5:
        raise ValueError(f"Conviction must be 1-5 (got {conviction})")
    if action not in ("enter", "add", "trim", "exit"):
        raise ValueError(f"Action must be enter/add/trim/exit (got {action})")

    with _write_lock:
        cursor = conn.execute(
            """INSERT INTO personal_decisions
               (timestamp, ticker, action, shares_delta, price, rationale,
                thesis_tags, conviction, target_price, stop_price,
                planned_exit_trigger, catalyst_dates, portfolio_snapshot, amends_id,
                late_entry)
               VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)""",
            (
                timestamp,
                ticker,
                action,
                shares_delta,
                price,
                rationale,
                json.dumps(thesis_tags),
                conviction,
                target_price,
                stop_price,
                planned_exit_trigger,
                json.dumps(catalyst_dates or []),
                json.dumps(portfolio_snapshot),
                amends_id,
                1 if late_entry else 0,
            ),
        )
        conn.commit()
        return cursor.lastrowid


def list_personal_decisions(conn: sqlite3.Connection, limit: int = 100) -> list[dict]:
    """Read personal/conviction decisions (newest first). Read-only."""
    rows = conn.execute(
        "SELECT id, timestamp, ticker, action, shares_delta, price, rationale, "
        "       thesis_tags, conviction, target_price, stop_price, "
        "       planned_exit_trigger, catalyst_dates, amends_id, late_entry "
        "FROM personal_decisions ORDER BY id DESC LIMIT ?",
        (limit,),
    ).fetchall()
    out = []
    for r in rows:
        d = dict(r)
        for k in ("thesis_tags", "catalyst_dates"):
            try:
                d[k] = json.loads(d[k]) if d[k] else []
            except Exception:
                pass
        d["late_entry"] = bool(d.get("late_entry"))
        out.append(d)
    return out
