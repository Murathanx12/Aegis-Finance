"""
Aegis Finance — Portfolio Intelligence Database
=================================================

Raw SQLite with WAL mode for the portfolio intelligence subsystem.
Forward-only schema versioning (no Alembic — overkill for 6 tables).

The rest of Aegis is stateless. This module adds persistence ONLY
for paper-portfolio state, trade log, audit log, and personal decisions.

Usage:
    from backend.db import init_db, get_connection, DB_PATH
"""

